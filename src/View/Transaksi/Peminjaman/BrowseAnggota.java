    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = table_anggota.getSelectedRow();
        Vector data = (Vector) data_anggota.getDataVector().elementAt(row);
        String nis = data.get(1).toString();
        String nama = data.get(2).toString();
        String kelas = data.get(3).toString();
        PD.nis = nis;
        PD.nama = nama;
        PD.kelas = kelas;
        PD.selectedDataAnggota();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

