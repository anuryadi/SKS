    public void get_anggota(String search) {
        
        ArrayList<ModelAnggota> list = new ControllerAnggota().viewWithSearch(search);
        data_anggota = (DefaultTableModel)table_anggota.getModel();
        data_anggota.getDataVector().removeAllElements();
        data_anggota.fireTableDataChanged();
        table_anggota.getColumnModel().getColumn(0).setMinWidth(0);
        table_anggota.getColumnModel().getColumn(0).setMaxWidth(0);
        table_anggota.getColumnModel().getColumn(0).setWidth(0);
        Object[] row = new Object[4];
        
        for(int i=0; i<list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getNis();
            row[2] = list.get(i).getNama();
            row[3] = list.get(i).getKelas();
            data_anggota.addRow(row);
        }
    }
   

