    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String text_nis = input_nis.getText(),
               code_book = input_kode_buku.getText(),
               jumlahS = input_jumlah.getText();
        Date dari = tanggal_pinjam.getDate();
        Date sampai = tanggal_kembali.getDate();
        
        if(text_nis.length() < 1) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih peminjam!");
            return;
        }
        
        if(code_book.length() < 1) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih buku!");
            return;
        }
        
        if(jumlahS.length() < 1) {
            JOptionPane.showMessageDialog(this, "Silahkan masukan jumlah buku!");
            return;
        }
        
        
        int quantity = Integer.parseInt(jumlahS);
        String dari_formated = new FormatDate().format(dari, "YYYY-MM-dd");
        String sampai_formated = new FormatDate().format(sampai, "YYYY-MM-dd");
        
        if(dari_formated == null || sampai_formated == null) {
            JOptionPane.showMessageDialog(this, "Silahkan masukan rentang tanggal dengan benar!");
            return;
        }
        
        Boolean action1 = new ControllerPeminjaman().create(nis, code_book, quantity, dari_formated, sampai_formated);
        Boolean action2 = new ControllerPeminjaman().update(code_book, quantity);

        if(action1 && action2) {
            JOptionPane.showMessageDialog(this, "Berhasil masukan data!");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal masukan data!");            
        }
        get_peminjam();
    }//GEN-LAST:event_submitActionPerformed

