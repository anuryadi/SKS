    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = table_peminjam.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Data belum terpilih!");
            return;
        }
        int idne = listPeminjam.get(row).getID();
        
        this.setVisible(false);
        JFrame pengembalian = new Pengembalians(idne);
        pengembalian.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void input_jumlahKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_input_jumlahKeyTyped
        // TODO add your handling code here:
        char enter = evt.getKeyChar();
        if(!(Character.isDigit(enter))){
            evt.consume();
        }
    }//GEN-LAST:event_input_jumlahKeyTyped

