    public void get_peminjam() {
        listPeminjam = new ControllerPeminjaman().viewPeminjaman(this.nis);
        DefaultTableModel model = (DefaultTableModel)table_peminjam.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Object[] row = new Object[7];
        for(int i=0; i<listPeminjam.size(); i++) {
            row[0] = listPeminjam.get(i).getID();
            row[1] = listPeminjam.get(i).getCodeBook();
            row[2] = listPeminjam.get(i).getTitle();
            row[3] = listPeminjam.get(i).getCategory();
            row[4] = listPeminjam.get(i).getQuantity();
            row[5] = listPeminjam.get(i).getTanggalPinjam();
            row[6] = listPeminjam.get(i).getTanggalKembali();
            model.addRow(row);
        }
    }
    
