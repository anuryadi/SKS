    private void detail_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detail_buttonActionPerformed
        // TODO add your handling code here:
        int row = table_peminjam.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "Data belum terpilih");
        }
        
        String nis = listPeminjam.get(row).getNis();
        
        this.setVisible(false);
        PeminjamanDetail pd = new PeminjamanDetail(nis);
        pd.setVisible(true);
    }//GEN-LAST:event_detail_buttonActionPerformed

