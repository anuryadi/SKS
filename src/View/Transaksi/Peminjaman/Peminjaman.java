    public void get_peminjam(String search, String dari, String sampai) {
        listPeminjam = new ControllerPeminjaman().viewTemplate(search, dari, sampai);
        DefaultTableModel model = (DefaultTableModel)table_peminjam.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Object[] row = new Object[7];
        for(int i=0; i<listPeminjam.size(); i++) {
            row[0] = i+1;
            row[1] = listPeminjam.get(i).getNis();
            row[2] = listPeminjam.get(i).getNama();
            row[3] = listPeminjam.get(i).getKelas();
            row[4] = listPeminjam.get(i).getQuantity();
            row[5] = listPeminjam.get(i).getLate();
            row[6] = listPeminjam.get(i).getJudulBuku();
            model.addRow(row);
        }
    }
    
