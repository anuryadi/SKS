    private void input_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_input_searchKeyReleased
        // TODO add your handling code here:
        Date dari = input_dari.getDate();
        Date sampai = input_sampai.getDate();
        String search = input_search.getText();
        String dari_formated;
        String sampai_formated;
        
        if (dari != null && sampai != null) {
            dari_formated = new FormatDate().format(dari, "YYYY-MM-dd");
            sampai_formated = new FormatDate().format(sampai, "YYYY-MM-dd");
        } else {
            dari_formated = "";
            sampai_formated = "";
        }
        
        get_peminjam(search, dari_formated, sampai_formated);
//        get_peminjam(search.get());
    }//GEN-LAST:event_input_searchKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Date dari = input_dari.getDate();
        Date sampai = input_sampai.getDate();
        if (dari == null || sampai == null) {
            JOptionPane.showMessageDialog(this, "Lengkapi filter tanggal!");
            return;
        }
        
        String search = input_search.getText();
        String dari_formated = new FormatDate().format(dari, "YYYY-MM-dd");
        String sampai_formated = new FormatDate().format(sampai, "YYYY-MM-dd");
        
        get_peminjam(search, dari_formated, sampai_formated);
    }//GEN-LAST:event_jButton1ActionPerformed

