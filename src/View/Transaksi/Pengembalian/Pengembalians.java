    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id = dataPengembalian.getID();
        String code_book = dataPengembalian.getCodeBook();
        int quantity = dataPengembalian.getQuantity();
        
        Boolean action1 = new ControllerPengembalian().update(id);
        Boolean action2 = new ControllerPengembalian().updateBook(code_book, quantity);

        if(action1 && action2) {
            JOptionPane.showMessageDialog(this, "Berhasil masukan data!");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal masukan data!");            
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

