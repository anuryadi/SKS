    public void getPeminjaman() {
        try {
            Date kembali_value = tanggal_kembali.getDate();
            String kembali = new FormatDate().format(kembali_value, "YYYY-MM-dd");
            dataPengembalian = new ControllerPengembalian().viewByID(ID, kembali);
            SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
            Date pinjams = sdf.parse(dataPengembalian.getTanggalPinjam());
            Date kembalis = sdf.parse(dataPengembalian.getTanggalKembali());
            tanggal_pinjam.setValue(pinjams);
            tanggal_kembali.setDate(kembalis);
            getAnggota(dataPengembalian.getNis());
            getBuku(dataPengembalian.getCodeBook());
            int telat = dataPengembalian.getDiff();
            int denda = telat * dataPengembalian.getQuantity() * 500;
            input_telat.setText(Integer.toString(telat));
            input_denda.setText(Integer.toString(denda));
        } catch (ParseException ex) {
            Logger.getLogger(View.Transaksi.Pengembalian.Pengembalians.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void getAnggota(String nis) {
        ModelAnggota ma = new ControllerAnggota().viewByNIS(nis);
        input_nis.setText(ma.getNis());
        input_nama.setText(ma.getNama());
    }
    
    public void getBuku(String code_book) {
        ModelBook mb = new ControllerBook().viewByCode(code_book);
        input_kode_buku.setText(mb.getKode());
        input_judul_buku.setText(mb.getJudul());
    }

