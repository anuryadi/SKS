    private void button_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_editActionPerformed
        // TODO add your handling code here:
        int row = table_anggota.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "Data belum terpilih!");
            return;
        }
        int ID = (int)table_anggota.getModel().getValueAt(row, 0);
        String nis = table_anggota.getModel().getValueAt(row, 1).toString();
        String nama = table_anggota.getModel().getValueAt(row, 2).toString();
        String kelas = table_anggota.getModel().getValueAt(row, 3).toString();
        
        input_nis.setText(nis);
        input_nama.setText(nama);
        input_kelas.setText(kelas);
        
        STATUS = 1;
        SELECTED_ID = ID;
    }//GEN-LAST:event_button_editActionPerformed

    private void button_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_submitActionPerformed
        // TODO add your handling code here:
        String nis =  input_nis.getText(),
        nama = input_nama.getText(),
        kelas = input_kelas.getText();

        Boolean action;

        if(STATUS == 0) {
            action = new ControllerAnggota().create(
                nis, 
                nama,
                kelas
            );
        } else {
            action = new ControllerAnggota().update(
                SELECTED_ID,
                nis, 
                nama,
                kelas
            );
        }

        if(action) {
            JOptionPane.showMessageDialog(this, "Data berhasil dimasukan!");
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal dimasukan!");
        }

        button_add.doClick();
        //        show_book();
    }//GEN-LAST:event_button_submitActionPerformed

