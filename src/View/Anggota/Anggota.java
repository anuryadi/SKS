    public void get_data() {
        
        ArrayList<ModelAnggota> list = new ControllerAnggota().view();
        DefaultTableModel model = (DefaultTableModel)table_anggota.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        table_anggota.getColumnModel().getColumn(0).setMinWidth(0);
        table_anggota.getColumnModel().getColumn(0).setMaxWidth(0);
        table_anggota.getColumnModel().getColumn(0).setWidth(0);
        Object[] row = new Object[4];
        for(int i=0; i<list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getNis();
            row[2] = list.get(i).getNama();
            row[3] = list.get(i).getKelas();
            model.addRow(row);
        }
    }

