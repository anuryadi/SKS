    public void get_category() {
        
        ArrayList<ModelBookCategory> list = new ControllerBookCategory().view();
        DefaultTableModel model = (DefaultTableModel)table_category.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        table_category.getColumnModel().getColumn(0).setMinWidth(0);
        table_category.getColumnModel().getColumn(0).setMaxWidth(0);
        table_category.getColumnModel().getColumn(0).setWidth(0);
        Object[] row = new Object[3];
        for(int i=0; i<list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getCodeCategory();
            row[2] = list.get(i).getTitle();
            model.addRow(row);
        }
    }

