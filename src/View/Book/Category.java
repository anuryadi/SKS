    private void button_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_editActionPerformed
        // TODO add your handling code here:
        int row = table_category.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "Data belum terpilih!");
            return;
        }
        int ID = (int)table_category.getModel().getValueAt(row, 0);
        String kode = table_category.getModel().getValueAt(row, 1).toString();
        String judul = table_category.getModel().getValueAt(row, 2).toString();
        
        input_kode.setText(kode);
        input_judul.setText(judul);
        
        STATUS = 1;
        SELECTED_ID = ID;
    }//GEN-LAST:event_button_editActionPerformed

    private void button_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_submitActionPerformed
        // TODO add your handling code here:
        String code_book =  input_kode.getText(),
        kode = input_kode.getText(),
        title = input_judul.getText();

        Boolean action;

        if(STATUS == 0) {
            action = new ControllerBookCategory().create(
                kode, 
                title
            );
        } else {
            action = new ControllerBookCategory().update(
                SELECTED_ID,
                kode, 
                title
            );
        }

        if(action) {
            JOptionPane.showMessageDialog(this, "Data berhasil dimasukan!");
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal dimasukan!");
        }

        button_add.doClick();
        //        show_book();
    }//GEN-LAST:event_button_submitActionPerformed

